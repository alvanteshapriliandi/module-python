# 2. Tipe Data dalam Python
# Python memiliki beberapa tipe data bawaan yang sering digunakan.

# 2.1 Integer (int)
# Bilangan bulat, bisa positif atau negatif.

# ✅ Contoh Integer:
angka1 = 10
angka2 = -20
angka3 = 1000000

print(type(angka1))

print('=============')

# 2.2 Float (float)
# Bilangan desimal atau pecahan.

# ✅ Contoh Float:

pi = 3.14
suhu = -12.5

print(type(pi))

print('=============')

# 2.3 String (str)
# Sekumpulan karakter yang diapit oleh tanda kutip tunggal atau ganda.

# ✅ Contoh String:

nama = "Alvan"
pesan = 'Halo, selamat datang'

print(type(nama))

# Operasi pada String:

teks = "Python"
print(teks.upper())     # PYTHON
print(teks.lower())     # python
print(teks[0])          # P (mengakses karakter pertama)
print(teks[-1])         # n (mengakses karakter terakhir)
print(teks[:3])         # Pyt (mengambil sebagian string)

